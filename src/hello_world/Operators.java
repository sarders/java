package hello_world;

public class Operators {
    public static void main(String[] args){
        int resto = 5 % 2;      // 2
        System.out.println(resto);
        resto++;                // incremento di 1
        System.out.println(resto);
        resto--;                // diminuisco di uno
        System.out.println(resto);
        resto+= 2;              // Equivale ad scrivere resto = resto + 2
        System.out.println(resto);
        resto*= 3;              // resto =  resto * 3;
        System.out.println(resto);
        resto/= 3;              // resto =  resto / 3;
        System.out.println(resto);
    }
}
